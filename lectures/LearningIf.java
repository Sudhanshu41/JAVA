public class LearningIf {
    public static void main(String[] args) {
        boolean isMale = true;
        String name = "Sudhanshu";
        System.out.println("before if");
        if(isMale){
            System.out.println("Mr."+name);
        }
        else {
            System.out.println("Mrs."+name);
        }
        System.out.println("After if");
    }
}
